public class OperadoresDeComparacao {
	public static void main(String[] args) {
		
		
		// Os operadores de comparação são usados para comparar dois
		// valores:

		// Igual a (==)
		// Diferente de (!=)
		// Maior que (>)
		// Menor que (<)
		// Maior ou igual a (>=)
		// Menor ou igual a (<=)

		// Exemplo:
		

		int a = 10;
		int b = 20;

		System.out.println(a == b); // Saída: false
		System.out.println(a != b); // Saída: true
		System.out.println(a > b);  // Saída: false
		System.out.println(a < b);  // Saída: true
		System.out.println(a >= b); // Saída: false
		System.out.println(a <= b); // Saída: true
		
	}
}